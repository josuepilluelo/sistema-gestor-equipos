    private void registrar_botonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrar_botonActionPerformed
        java.util.Date date = new java.util.Date();
        SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
        Date fecha_1 = new Date();
        int validacion = 0;
        String nombre_empleado, area;
        String fecha = "";
        nombre_empleado = barra_empleado.getText().trim().toUpperCase();
        area = barra_area.getText().trim().toUpperCase();
        fecha = f.format(fecha_1);

        if (nombre_empleado.equals("")) {
            barra_empleado.setBackground(Color.red);
            validacion++;
        }
        if (area.equals("")) {
            barra_area.setBackground(Color.red);
            validacion++;
        }

        if (validacion == 0) {
            try {
                Connection cn = Conection_bd.conexion_bd();
                PreparedStatement pst = cn.prepareStatement("insert into responsable values(?,?,?,?)");
                pst.setInt(1, 0);
                pst.setString(2, nombre_empleado);
                pst.setString(3, "ACTIVO");
                pst.setString(4, area);
                pst.executeUpdate();
                cn.close();

            } catch (Exception e) {
                System.out.println("Error al guardar: " + e);
                //JOptionPane.showMessageDialog(Componente, mensaje, t√≠tulo, tipo de mensaje, icono);
                JOptionPane.showMessageDialog(null, "Error al guardar informacion\n"+e, "Error", JOptionPane.ERROR_MESSAGE);
            }
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Debes llenar los campos requeridos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_registrar_botonActionPerformed

