    public void limpiarCampos() {
        barra_area.setBackground(Color.GREEN);
        barra_empleado.setBackground(Color.GREEN);

        Icon icono_correcto = new ImageIcon(getClass().getResource("/imagenes/correcto.png"));
        int valor = JOptionPane.showConfirmDialog(null, "Se ha guardado correctamente", "Responsable registrado correctamente", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, icono_correcto);
        //int valor = JOptionPane.showConfirmDialog(null, "Se ha guardado correctamente", "Equipo registrado correctamente", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, icono_correcto);
        if (valor == JOptionPane.YES_OPTION) {
            barra_area.setText("");
            barra_empleado.setText("");
        }
        barra_area.setBackground(Color.white);
        barra_empleado.setBackground(Color.white);

    }

